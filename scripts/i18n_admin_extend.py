def merge_into(target: dict, additions: dict):
    """Recursively merge additions into target (target wins for existing keys)."""
    for k, v in additions.items():
        if k in target and isinstance(target[k], dict) and isinstance(v, dict):
            merge_into(target[k], v)
        elif k not in target:
            target[k] = v


for lang, extensions in EXTENSIONS.items():
    p = ADMIN_LOCALES / lang / "common.json"
    data = json.loads(p.read_text(encoding="utf-8-sig"))
    pages = data.setdefault("pages", {})

    # profile
    if "profile" in extensions:
        profile = pages.setdefault("profile", {})
        merge_into(profile, extensions["profile"])

    # acceptInvite
    if "acceptInvite" in extensions:
        ai = pages.setdefault("acceptInvite", {})
        merge_into(ai, extensions["acceptInvite"])

    # nested history.history (page.history.history.bankChanges etc.)
    history_outer = pages.setdefault("history", {})
    history_inner = history_outer.setdefault("history", {})

    if "history_history_bankChanges" in extensions:
        bc = history_inner.setdefault("bankChanges", {})
        merge_into(bc, extensions["history_history_bankChanges"])
    if "history_history_depositSources" in extensions:
        ds = history_inner.setdefault("depositSources", {})
        merge_into(ds, extensions["history_history_depositSources"])
    if "history_history_linkedAccounts" in extensions:
        la = history_inner.setdefault("linkedAccounts", {})
        merge_into(la, extensions["history_history_linkedAccounts"])

    # Write back with 2-space indent
    p.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"updated {lang}")

print("done")
